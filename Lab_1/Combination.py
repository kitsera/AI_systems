class Combination:

    def __init__(self, first, second, distance):
        self.first = first
        self.second = second
        self.distance = distance

    def get_first(self):
        return self.first

    def set_first(self, first):
        self.first = first

    def get_second(self):
        return self.second

    def set_second(self, second):
        self.second = second

    def get_dist(self):
        return self.distance

    def set_dist(self, dist):
        self.distance = dist
